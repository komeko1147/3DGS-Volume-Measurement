import { mkdir, rm, copyFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { spawn } from 'node:child_process';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, '..');
const splatTransformCli = path.join(projectRoot, 'node_modules', '@playcanvas', 'splat-transform', 'bin', 'cli.mjs');

const generatedDir = path.join(projectRoot, 'generated', 'test-splats');
const examplesSplatsDir = path.join(projectRoot, 'official-engine', 'examples', 'assets', 'splats');
const examplesVolumesDir = path.join(projectRoot, 'official-engine', 'examples', 'assets', 'volumes');
const distSplatsDir = path.join(projectRoot, 'official-engine', 'examples', 'dist', 'static', 'assets', 'splats');
const distVolumesDir = path.join(projectRoot, 'official-engine', 'examples', 'dist', 'static', 'assets', 'volumes');

const SH_C0 = 0.28209479177387814;
const OPACITY_LOGIT = 6;
const SPACING = 0.007; //0.08
const SPLAT_RADIUS = 0.006; //0.045
const SCALE_LOG = Math.log(SPLAT_RADIUS);

const rgbToDc = (value) => (value - 0.5) / SH_C0;

const SHAPE_CONFIGS = [
    {
        name: 'testCube',
        voxelParams: '0.03,0.01',  //'0.02,0.02'
        color: [0.95, 0.35, 0.25],
        referenceVolume: 1,
        generatePoints: () => {
            const points = [];
            const half = 0.5;
            for (let x = -half; x <= half + 1e-6; x += SPACING) {
                for (let y = -half; y <= half + 1e-6; y += SPACING) {
                    for (let z = -half; z <= half + 1e-6; z += SPACING) {
                        points.push([x, y, z]);
                    }
                }
            }
            return points;
        }
    },
    {
        name: 'testSphere',
        voxelParams: '0.03,0.01',  //'0.02,0.02'
        color: [0.25, 0.55, 0.95],
        referenceVolume: (4 / 3) * Math.PI * Math.pow(0.5, 3),
        generatePoints: () => {
            const points = [];
            const radius = 0.5;
            const radiusSq = radius * radius;
            for (let x = -radius; x <= radius + 1e-6; x += SPACING) {
                for (let y = -radius; y <= radius + 1e-6; y += SPACING) {
                    for (let z = -radius; z <= radius + 1e-6; z += SPACING) {
                        if (x * x + y * y + z * z <= radiusSq) {
                            points.push([x, y, z]);
                        }
                    }
                }
            }
            return points;
        }
    },
    {
        name: 'testCylinder',
        voxelParams: '0.03,0.01',  //'0.02,0.02'
        color: [0.3, 0.9, 0.45],
        referenceVolume: Math.PI * Math.pow(0.35, 2) * 1,
        generatePoints: () => {
            const points = [];
            const radius = 0.35;
            const height = 1.0;
            const radiusSq = radius * radius;
            const halfHeight = height * 0.5;
            for (let x = -radius; x <= radius + 1e-6; x += SPACING) {
                for (let y = -halfHeight; y <= halfHeight + 1e-6; y += SPACING) {
                    for (let z = -radius; z <= radius + 1e-6; z += SPACING) {
                        if (x * x + z * z <= radiusSq) {
                            points.push([x, y, z]);
                        }
                    }
                }
            }
            return points;
        }
    }
];

const run = (command, args, cwd) => {
    return new Promise((resolve, reject) => {
        const child = spawn(command, args, {
            cwd,
            stdio: 'inherit',
            shell: false
        });

        child.on('error', reject);
        child.on('exit', (code) => {
            if (code === 0) {
                resolve();
                return;
            }

            reject(new Error(`Command failed with exit code ${code}: ${command} ${args.join(' ')}`));
        });
    });
};

const writeBinaryPly = async (filename, points, color, referenceVolume) => {
    const propertyNames = [
        'x', 'y', 'z',
        'rot_0', 'rot_1', 'rot_2', 'rot_3',
        'scale_0', 'scale_1', 'scale_2',
        'f_dc_0', 'f_dc_1', 'f_dc_2',
        'opacity'
    ];

    const header = [
        'ply',
        'format binary_little_endian 1.0',
        `comment generated by generate-test-splats.mjs`,
        `comment reference_volume ${referenceVolume}`,
        `element vertex ${points.length}`,
        ...propertyNames.map((name) => `property float ${name}`),
        'end_header'
    ].join('\n') + '\n';

    const floatsPerVertex = propertyNames.length;
    const buffer = Buffer.allocUnsafe(points.length * floatsPerVertex * 4);
    const dc0 = rgbToDc(color[0]);
    const dc1 = rgbToDc(color[1]);
    const dc2 = rgbToDc(color[2]);

    let offset = 0;
    for (const [x, y, z] of points) {
        const values = [
            x, y, z,
            1, 0, 0, 0,
            SCALE_LOG, SCALE_LOG, SCALE_LOG,
            dc0, dc1, dc2,
            OPACITY_LOGIT
        ];

        for (const value of values) {
            buffer.writeFloatLE(value, offset);
            offset += 4;
        }
    }

    await mkdir(path.dirname(filename), { recursive: true });
    await writeFile(filename, Buffer.concat([Buffer.from(header, 'ascii'), buffer]));
};

const copyToEditorAssets = async (basename) => {
    const files = [
        `${basename}.ply`,
        `${basename}.voxel.json`,
        `${basename}.voxel.bin`,
        `${basename}.collision.glb`
    ];

    for (const file of files) {
        const source = path.join(generatedDir, file);
        const targetDir = file.endsWith('.ply') ? examplesSplatsDir : examplesVolumesDir;
        const distTargetDir = file.endsWith('.ply') ? distSplatsDir : distVolumesDir;
        await mkdir(targetDir, { recursive: true });
        await mkdir(distTargetDir, { recursive: true });
        await copyFile(source, path.join(targetDir, file));
        await copyFile(source, path.join(distTargetDir, file));
    }
};

const main = async () => {
    await rm(generatedDir, { recursive: true, force: true });
    await mkdir(generatedDir, { recursive: true });

    for (const shape of SHAPE_CONFIGS) {
        const points = shape.generatePoints();
        const plyPath = path.join(generatedDir, `${shape.name}.ply`);
        const voxelPath = path.join(generatedDir, `${shape.name}.voxel.json`);

        console.log(`\n=== Generating ${shape.name} ===`);
        console.log(`Reference volume: ${shape.referenceVolume.toFixed(6)}`);
        console.log(`Point count: ${points.length}`);

        await writeBinaryPly(plyPath, points, shape.color, shape.referenceVolume);

        await run(
            process.execPath,
            [
                splatTransformCli,
                '-w',
                '--voxel-params',
                shape.voxelParams,
                '-K',
                plyPath,
                voxelPath
            ],
            projectRoot
        );

        await copyToEditorAssets(shape.name);
    }

    console.log('\nGenerated test splat assets:');
    console.log(generatedDir);
};

main().catch((error) => {
    console.error('\nFailed to generate test splats.');
    console.error(error);
    process.exitCode = 1;
});
